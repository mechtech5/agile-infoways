    {
        $items = Item::paginate(10);
        $cart = Cart::with('items')->where('user_id', auth()->user()->id)->first();

        // Convert to array of ids
        $cart_items = array_map(function ($items) {
            return ($items['id']);
        }, $cart->items->toArray());

        return view('items.index', compact('items', 'cart_items'));
